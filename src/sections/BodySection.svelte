<div class="container">
  <h1>Financial Well-Being</h1>
  <p>First, let’s take a look at what is financial well-being.</p>

  <p>
    According to the <a
      href="https://hr.umich.edu/benefits-wellness/health-well-being/mhealthy/faculty-staff-well-being/financial-well-being"
      >University of Michigan Human Resources</a
    >:
  </p>

  <p>
    “Being financially well means you can meet your current and ongoing
    financial obligations, feel secure in your financial future, and are able to
    make choices that allow you to enjoy life – in other words, financial
    freedom.”
  </p>

  <p>
    Factors that can contribute to or detract from financial well-being include
    employment, income and expenses, banking and credit, assets and debts,
    housing costs, higher education, loans, investments, etc.
  </p>

  <p>
    Despite the plethora of data showing the financial disparities and distance
    between the Black population and other wealthier racial groups, I
    surprisingly found some data showing some improvements for the Black
    population in the following areas: Assets and Debts, Education, Employment,
    and Business.
  </p>

  <h2>
    <a href="https://blackwealthdata.org/explore/assets">Assets and Debts</a>
  </h2>

  <h3>1. Median Total Net Worth for Black Households increased</h3>

  <!-- Data graphic 2  -->
  <img
    class="median-net-worth-img"
    src="median-net-worth.png"
    alt="Median Net Worth"
  />

  <p>
    From 2019 to 2022, Black households’ median total net worth jumped from
    $28,000 to $44,000.
  </p>

  <!-- Data graphic 3  -->
  <img
    class="covid-19-explanation-img"
    src="covid-19-explanation.png"
    alt="COVID-19 Explanation"
  />

  <p>
    According to the explanation that appears in this data graphic when you
    hover your cursor over the vertical dotted line above 2020, in the years
    following the onset of the COVID-19 pandemic in 2020, factors such as <strong
      >wage growth</strong
    >, <strong>government support</strong> (such as
    <strong>stimulus checks</strong>), and
    <strong>rising home values</strong>
    contributed to Americans’ wealth.
  </p>

  <p>
    Most notably, “Black households experienced the <strong
      >biggest percentage increase</strong
    >
    as <strong>median net worth rose 59 percent</strong> to $44,100.”
  </p>

  <h3>2. Median Total Assets for Black Households increased</h3>

  <!-- Data graphic 4  -->
  <img class="median-assets-img" src="median-assets.png" alt="Median Assets" />

  <p>
    From 2019 to 2022, Black households’ median total assets increased from
    $61,000 to $106,000.
  </p>

  <!-- Data graphic 5  -->
  <img class="median-debt-img" src="median-debt.png" alt="Median Debt" />

  <p>
    On the other hand, Black households’ median total debt also increased from
    $32,000 in 2019 to $45,000 in 2022. However, Black households have lower
    debts compared to White and Other non-Hispanic households.
  </p>

  <h3>3. Percent of Black Households Holding Asset Types increased</h3>

  <p>
    The percentage of Black households holding various asset types has increased
    over the years.
  </p>

  <!-- Data graphic 6  -->
  <img
    class="directly-held-stocks-img"
    src="directly-held-stocks.png"
    alt="Directly Held Stocks"
  />

  <!-- Data graphic 7  -->
  <img
    class="value-household-stock-img"
    src="value-household-stock.png"
    alt="Value Household Stock"
  />

  <p>
    For example, from 2019 to 2022, the percent of Black households holding
    <strong>directly held stocks</strong> nearly doubled from 6.7% to 13.2%,
    while Black households’
    <strong> total value of household stock holdings</strong> increased from 33.5%
    to 39.2%.
  </p>

  <h3>4. Two of Black Households’ Debt Types decreased</h3>

  <p>
    In terms of debt, the percent of Black households holding debt types has
    increased for all debt types.
  </p>

  <p>
    However, when looking at debt for Black households from 2019 to 2022, two
    debt categories have seen decreases from 2019 to 2022.
  </p>

  <!-- Data graphic 8  -->
  <img
    class="debt-primary-residence-img"
    src="debt-primary-residence.png"
    alt="Debt Primary Residence"
  />

  <!-- Data graphic 9  -->
  <img
    class="education-loans-img"
    src="education-loans.png"
    alt="Education Loans"
  />

  <p>
    From 2019 to 2022, Black households’ median <strong
      >debt secured by primary residence</strong
    >
    decreased from $134,468 to $111,000, and Black households’ median
    <strong>education loans</strong> decreased from $34,776 to $26,000.
  </p>

  <h3>5. Black Unbanked Households decreased</h3>

  <p>
    While Black unbanked households may have the highest percentage among all
    racial groups, the percent of Black unbanked households decreased over time
    from 2017 to 2023.
  </p>

  <!-- Data graphic 10  -->
  <img
    class="percent-unbanked-img"
    src="percent-unbanked.png"
    alt="Percent Unbanked"
  />

  <p>
    From 2019 to 2022, the percent of Black unbanked households dropped from
    13.8% to 10.6%.
  </p>

  <p>
    This decrease could mean that fewer Black households may face the negative
    consequences of being unbanked, which can include facing challenges in
    building a good credit history and not having access to certain financial
    services and opportunities that can help them save money and build their
    financial wealth.
  </p>

  <p>
    Having bank accounts can help people gain access to financial institutions’
    services and open up more possibilities for economic mobility.
  </p>

  <h2>
    <a href="https://blackwealthdata.org/explore/education">Education</a>
  </h2>

  <h3>6. Black High School and College Graduates increased</h3>

  <p>
    Over the years, the percentage of Black students who completed high school
    and who obtained a bachelor’s degree or higher has increased.
  </p>

  <!-- Data graphic 11  -->
  <img
    class="high-school-attainment-img"
    src="high-school-attainment.png"
    alt="High School Attainment"
  />

  <!-- Data graphic 12  -->
  <img
    class="bachelor-degree-attainment-img"
    src="bachelor-degree-attainment.png"
    alt="Bachelor Degree Attainment"
  />

  <p>
    From 2019 to 2023, the percent of Black students who completed high school
    or higher increased from 88.8% to 91.3%, and the percent of Black students
    who obtained a bachelor’s degree or higher increased from 26.3% to 29.0%.
  </p>

  <p>
    Having a high school diploma and a bachelor’s degree or higher can not only
    equip students with more knowledge and experience in various fields but also
    help them meet qualifications for entry-level or field-specific jobs,
    expanding their career opportunities and thus contributing to their
    financial well-being.
  </p>

  <h2>
    <a href="https://blackwealthdata.org/explore/employment">Employment</a>
  </h2>

  <h3>7. Black Median Annual Earnings increased</h3>

  <p>
    From 2019 to 2023, median annual earnings for full-time Black workers aged
    25-34 increased overall from $43,660 to $46,040.
  </p>

  <!-- Data graphic 13  -->
  <img
    class="median-earnings-all-img"
    src="median-earnings-all.png"
    alt="Median Earnings All"
  />

  <!-- Data graphic 14  -->
  <img
    class="median-earnings-black-img"
    src="median-earnings-black.png"
    alt="Median Earnings Black"
  />

  <p>
    Having more income leads to having more money to pay off expenses, invest to
    gain more money, and improve Black financial well-being.
  </p>
